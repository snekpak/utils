import bcrypt
#-----------------------------><-----------------------------#


def hash_key(p):
    return bcrypt.hashpw(p.encode('utf-8'), bcrypt.gensalt())

def check_key(stored, prov):
    return bcrypt.checkpw(prov.encode('utf-8'), stored)


